export function searchNutrients(query) {
  if (!query || query.trim().length === 0) return [];
  const q = query.toLowerCase().trim();
  return nutrientData.filter(n => n.name.toLowerCase().includes(q) || n.brief.toLowerCase().includes(q));
}

export function getNutrientById(id) {
  return nutrientData.find(n => n.id === id);
}

export function getNutrientsList() {
  return nutrientData;
}